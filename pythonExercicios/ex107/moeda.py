def aumentar(num, pct):
    vlrMaior = 0
    vlrMaior = (num +((num * pct) / 100))
    return vlrMaior


def diminuir(num, pct):
    vlrMenor = 0
    vlrMenor = (num -((num * pct) / 100))
    return vlrMenor


def metade(num):
    vlrmetade = 0
    vlrmetade = num / 2
    return vlrmetade


def dobro(num):
    vlrdobro = 0
    vlrdobro = num * 2
    return vlrdobro

